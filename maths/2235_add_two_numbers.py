'''
Problem Description:
Given two integers num1 and num2,
return their sum.

Approach:
- Directly return the sum using the + operator.
- No special handling is required since integers
  are directly supported in Python.

Time Complexity:
O(1)

Space Complexity:
O(1)
'''
class Solution(object):
    def sum(self, num1, num2):
        """
        :type num1: int
        :type num2: int
        :rtype: int
        """
        return num1 + num2
